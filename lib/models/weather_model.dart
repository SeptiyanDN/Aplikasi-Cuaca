class Forecast {
  List<Forecastday>? forecastday;

  Forecast({this.forecastday});

  Forecast.fromJson(Map<String, dynamic> json) {
    if (json['forecastday'] != null) {
      forecastday = <Forecastday>[];
      json['forecastday'].forEach((v) {
        forecastday!.add(new Forecastday.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    if (this.forecastday != null) {
      data['forecastday'] = this.forecastday!.map((v) => v.toJson()).toList();
    }
    return data;
  }
}

class Forecastday {
  String? date;
  int? dateEpoch;
  List<Hour>? hour;

  Forecastday({this.date, this.dateEpoch, this.hour});

  Forecastday.fromJson(Map<String, dynamic> json) {
    date = json['date'];
    dateEpoch = json['date_epoch'];
    if (json['hour'] != null) {
      hour = <Hour>[];
      json['hour'].forEach((v) {
        hour!.add(new Hour.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    data['date'] = this.date;
    data['date_epoch'] = this.dateEpoch;
    if (this.hour != null) {
      data['hour'] = this.hour!.map((v) => v.toJson()).toList();
    }
    return data;
  }
}

class Hour {
  Condition? condition;
  String? time;
  double? tempC;
  double? windMph;
  int? humidity;
  int? cloud;

  Hour(
      {this.condition,
      this.time,
      this.tempC,
      this.windMph,
      this.humidity,
      this.cloud});

  Hour.fromJson(Map<String, dynamic> json) {
    condition = json['condition'] != null
        ? new Condition.fromJson(json['condition'])
        : null;
    time = json['time'];
    tempC = json['temp_c'];
    windMph = json['wind_mph'];
    humidity = json['humidity'];
    cloud = json['cloud'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    if (this.condition != null) {
      data['condition'] = this.condition!.toJson();
    }
    data['time'] = this.time;
    data['temp_c'] = this.tempC;
    data['wind_mph'] = this.windMph;
    data['humidity'] = this.humidity;
    data['cloud'] = this.cloud;
    return data;
  }
}
